#include <stdio.h>

int main(int argc,char *argv[]){

    printf("mayor que: %i \n", 4>3); //Si esta comparacion es cierta nos devuelve un 1
    printf("mayor que: %i \n", 4<3); //si la comparacion no es cierta devuelve 0
    
    printf("mayor o igual que: %i \n", 4>=3); //mayor o igual que
    printf("menor o igual que: %i \n", 4<=3); //menor o igual que
    
    printf("igualdad que: %i\n", 4==3); //igualdad
    printf("desigualdad que: %i\n", 4!=3); //diferente a
    
    return 0;
}

